package main

import "fmt"

func main() {
	//* Define number data type, integer and float
	fmt.Println("Satu:", 1)
	fmt.Println("Negatif tiga:", -3)
	fmt.Println("Tiga koma lima:", 3.5)
	println("======")
	//* Define boolean data type, true or false
	fmt.Println("True: ", true)
	fmt.Println("False: ", false)
	println("=======")
	//* Define string data types
	fmt.Println("Ahmad")
	fmt.Println("Ahmad Khoirul")
	fmt.Println("Ahmad Khoirul Muslimin")
	println("=======")
	fmt.Println("Length characters of 'Ahmad': ", len("Ahmad"))
	//* len() for count length of data source in parameters
}